package by.htp.library.service;

public class PrintEditionServiceException extends Exception {

	public PrintEditionServiceException() {
		super();
	}

	public PrintEditionServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PrintEditionServiceException(String arg0) {
		super(arg0);
	}

	public PrintEditionServiceException(Throwable arg0) {
		super(arg0);
	}

}
